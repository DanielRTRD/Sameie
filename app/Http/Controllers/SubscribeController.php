    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // grab the user
        $user = auth()->user();

        if (!$user->hasPaymentMethod()) {
            return redirect()->route('pm.index')->with('status', 'Legg til et kort her.');
        }

        $plan = $request->input('plan');
        ///dd($plan);

        // grab the credit card token
        $paymentMethod = $user->defaultPaymentMethod()->paymentMethod;

        // create the subscription
        try {
            $user->newSubscription('kontigent', $plan)->create($paymentMethod, [
                'email' => $user->email
            ]);
        } catch (\Exception $e) {
            return back()->withErrors(['message' => 'Error creating subscription: '.$e->getMessage()]);
        }
        
        return redirect()->route('dashboard')->with('status', 'Subscribed.');
    }

